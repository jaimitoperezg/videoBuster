    private void btnAgregarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCatActionPerformed
        boolean verifica = true;

        if(this.txtAddCat.getText().equals("") || this.txtAddCat.getText().equals(" ") || this.txtAddCat.getText().equals("  ")) {
            verifica = false;
            JOptionPane.showMessageDialog(this, "El campo descripciòn no puede estar vacío");
        }

        if(verifica == true) {
            Categoria cate;
            cate = new Categoria();
            
            cate.setId(Integer.parseInt(this.txtCodCat.getText()));
            cate.setDescripcion(this.txtAddCat.getText().toUpperCase().trim());

            if(Registro.actualizarCat(cate))
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Categoría actualizada");
            }
            else
            {
                javax.swing.JOptionPane.showMessageDialog(this, "No se actualizó la categoría");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tienes un error. Por favor ingresa correctamente todos los datos.");
        }
    }//GEN-LAST:event_btnAgregarCatActionPerformed

    private void txtCodCatKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodCatKeyTyped
        char vchar = evt.getKeyChar(); if (!(Character.isDigit(vchar)
               || (vchar == KeyEvent.VK_BACK_SPACE)
               || (vchar == KeyEvent.VK_DELETE))) {
                    evt.consume();
                }
    }//GEN-LAST:event_txtCodCatKeyTyped

    private void btnObtenerCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObtenerCatActionPerformed
        boolean verifica = true;
        String cateDesc = Registro.getCatDesc(Integer.parseInt(this.txtCodCat.getText()));
        
        if(cateDesc == null) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un número de código válido para poder actualizar una Categoría (Ejemplo: 1, 2, 3...)");
            txtCodCat.setText("");
        } else {
            txtAddCat.setText(cateDesc);
        }
    }//GEN-LAST:event_btnObtenerCatActionPerformed

